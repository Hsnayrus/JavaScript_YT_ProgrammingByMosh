// Array of colors selected by the user
let selectedColors = ["red", "green"];
// Arrays in JS are 0-indexed
console.log(selectedColors[0]);
// The contents and length of an array are dynamic in JS, meaning it can change at runtime
// Adds a new element to the array
selectedColors[2] = "blue";
console.log(selectedColors);
selectedColors[5] = 1;
console.log(selectedColors);
